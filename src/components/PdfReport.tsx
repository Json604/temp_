// --- Page 3: Quantitative Projections ---

function Projections({
  inputs,
  derived,
  results,
}: {
  inputs: ProcessInputs;
  derived: DerivedParameters;
  results: PartialAssessmentResult;
}) {
  const { otr, mixing, shear, co2, heat } = results;
  const vPilot = pilotVolume(inputs.v_lab, inputs.v_target);
  const pilot = computePilot(inputs, derived);

  const labTipSpeed = Math.PI * derived.n_rps * derived.lab_geometry.d_imp;
  const heatKwM3Lab = METABOLIC_HEAT_FACTOR * derived.our_peak;
  const heatKwM3Target =
    derived.target_geometry.volume_m3 > 0
      ? heat.q_metabolic / derived.target_geometry.volume_m3
      : 0;

  const rows: { param: string; lab: string; pilotVal: string; prod: string }[] = [
    {
      param: "kLa achievable (h\u207B\u00B9)",
      lab: fmt(otr.kla_lab),
      pilotVal: fmt(pilot.klaAchievable),
      prod: fmt(otr.kla_target_moderate),
    },
    {
      param: "kLa required (h\u207B\u00B9)",
      lab: fmt(otr.kla_required),
      pilotVal: fmt(otr.kla_required),
      prod: fmt(otr.kla_required),
    },
    {
      param: "Mixing time (s)",
      lab: fmt(mixing.theta_mix_lab),
      pilotVal: fmt(pilot.mixingTime),
      prod: fmt(mixing.theta_mix_target),
    },
    {
      param: "Da (mixing)",
      lab: "\u2014",
      pilotVal: pilot.da != null ? fmt(pilot.da, 3) : "\u2014",
      prod: mixing.da != null ? fmt(mixing.da, 3) : "\u2014",
    },
    {
      param: "Tip speed (m/s)",
      lab: fmt(labTipSpeed),
      pilotVal: fmt(pilot.tipSpeed),
      prod: fmt(shear.tip_speed),
    },
    {
      param: "pCO\u2082 bottom (bar)",
      lab: "\u2014",
      pilotVal: pilot.pco2Bottom != null ? fmt(pilot.pco2Bottom, 3) : "\u2014",
      prod: co2.pco2_bottom != null ? fmt(co2.pco2_bottom, 3) : "\u2014",
    },
    {
      param: "Metabolic heat (kW/m\u00B3)",
      lab: fmt(heatKwM3Lab, 2),
      pilotVal: fmt(pilot.heatKwM3, 2),
      prod: fmt(heatKwM3Target, 2),
    },
  ];

  return (
    <Page size="A4" style={s.page}>
      <PageHeader />
      <Text style={s.sectionTitle}>Quantitative Projections</Text>

      <Text style={s.bodyText}>
        Projections across three scales: lab ({fmtInt(inputs.v_lab)} L), pilot ({fmtInt(vPilot)} L, geometric mean), and production ({fmtInt(inputs.v_target)} L). All values computed at constant P/V (moderate scenario, 1.0\u00D7).
      </Text>

      <View style={s.projTable}>
        <View style={s.projHeader}>
          <Text style={[s.projHeaderCell, s.projParamCol]}>Parameter</Text>
          <Text style={[s.projHeaderCell, s.projValCol]}>
            Lab ({fmtInt(inputs.v_lab)} L)
          </Text>
          <Text style={[s.projHeaderCell, s.projValCol]}>
            Pilot ({fmtInt(vPilot)} L)
          </Text>
          <Text style={[s.projHeaderCell, s.projValCol]}>
            Production ({fmtInt(inputs.v_target)} L)
          </Text>
        </View>
        {rows.map((row, i) => (
          <View key={i} style={i % 2 === 0 ? s.projRow : s.projRowAlt}>
            <Text style={[{ fontSize: 8 }, s.projParamCol]}>{row.param}</Text>
            <Text style={[{ fontSize: 8, fontFamily: "Courier" }, s.projValCol]}>
              {row.lab}
            </Text>
            <Text style={[{ fontSize: 8, fontFamily: "Courier" }, s.projValCol]}>
              {row.pilotVal}
            </Text>
            <Text style={[{ fontSize: 8, fontFamily: "Courier" }, s.projValCol]}>
              {row.prod}
            </Text>
          </View>
        ))}
      </View>

      {/* Additional context */}
      <Text style={s.subsectionTitle}>Scale-Up Basis</Text>
      <View style={s.paramTable}>
        <View style={s.paramTableRow}>
          <Text style={s.paramTableLabel}>Scale-up rule</Text>
          <Text style={s.paramTableValue}>Constant P/V</Text>
        </View>
        <View style={s.paramTableRow}>
          <Text style={s.paramTableLabel}>P/V (lab)</Text>
          <Text style={s.paramTableValue}>{fmt(derived.pv_lab)} W/m{"\u00B3"}</Text>
        </View>
        <View style={s.paramTableRow}>
          <Text style={s.paramTableLabel}>Impeller type</Text>
          <Text style={s.paramTableValue}>{inputs.impeller_type}</Text>
        </View>
        <View style={s.paramTableRow}>
          <Text style={s.paramTableLabel}>H/D (target)</Text>
          <Text style={s.paramTableValue}>{fmt(inputs.h_d_target, 1)}</Text>
        </View>
        <View style={s.paramTableRow}>
          <Text style={s.paramTableLabel}>Impellers (target)</Text>
          <Text style={s.paramTableValue}>{inputs.n_impellers}</Text>
        </View>
        <View style={s.paramTableRow}>
          <Text style={s.paramTableLabel}>VVM</Text>
          <Text style={s.paramTableValue}>{fmt(inputs.vvm, 1)}</Text>
        </View>
      </View>

      <PageFooter />
    </Page>
  );
}

