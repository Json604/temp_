// --- Helpers ---

function fmt(n: number, decimals = 1): string {
  return n.toFixed(decimals);
}

function fmtInt(n: number): string {
  return Math.round(n).toString();
}

function riskLabel(score: RiskScore): string {
  return score.charAt(0).toUpperCase() + score.slice(1);
}

function confidenceLabel(c: Confidence): string {
  switch (c) {
    case "high_confidence":
      return "High-confidence";
    case "reliable":
      return "Reliable";
    case "directional":
      return "Directional";
  }
}

function klaVantRiet(pv: number, vs: number): number {
  const kla_s =
    VANT_RIET_COALESCING_COEFFICIENT *
    Math.pow(pv, VANT_RIET_COALESCING_PV_EXPONENT) *
    Math.pow(vs, VANT_RIET_COALESCING_VS_EXPONENT);
  return kla_s * 3600;
}

function pilotVolume(vLab: number, vTarget: number): number {
  return Math.sqrt(vLab * vTarget);
}

function countEstimatedParams(inputs: ProcessInputs): {
  provided: number;
  estimated: number;
  total: number;
} {
  let provided = 0;
  let estimated = 0;

  // Always provided
  const alwaysProvided: (keyof ProcessInputs)[] = [
    "organism_class",
    "organism_species",
    "process_type",
    "v_lab",
    "v_target",
    "rpm",
    "biomass",
  ];
  provided += alwaysProvided.length;

  // Conditionally provided vs estimated
  if (inputs.our_mode === "measured" && inputs.our_measured != null) {
    provided += 1;
  } else if (inputs.our_mode === "exhaust_gas") {
    provided += 1;
  } else {
    estimated += 1; // OUR estimated from literature
  }

  // Optional params that have defaults
  const optionalWithDefaults: { key: keyof ProcessInputs; defaultCheck: boolean }[] = [
    { key: "h_d_lab", defaultCheck: inputs.h_d_lab === 1.2 },
    { key: "h_d_target", defaultCheck: false },
    { key: "n_impellers", defaultCheck: false },
    { key: "impeller_type", defaultCheck: inputs.impeller_type === "rushton" },
    { key: "vvm", defaultCheck: inputs.vvm === 1.0 },
    { key: "do_setpoint", defaultCheck: inputs.do_setpoint === 30 },
    { key: "temperature", defaultCheck: false },
    { key: "t_cw_inlet", defaultCheck: inputs.t_cw_inlet === 12 },
  ];

  for (const p of optionalWithDefaults) {
    provided += 1; // All are user-provided or defaulted
  }

  const total = provided + estimated;
  return { provided, estimated, total };
}

function getHighImpactAssumptions(
  inputs: ProcessInputs,
  derived: DerivedParameters,
  results: PartialAssessmentResult,
): string[] {
  const assumptions: string[] = [];

  if (inputs.our_mode === "estimate") {
    assumptions.push(
      `OUR estimated from literature midpoint for ${SPECIES_LABELS[inputs.organism_species] ?? inputs.organism_species}. ` +
        `Actual OUR may range from ${fmt(derived.our_min ?? 0)} to ${fmt(derived.our_max ?? 0)} mmol/L/h. ` +
        `Higher OUR increases OTR, heat, and CO\u2082 risk; lower OUR decreases them.`,
    );
  }

  assumptions.push(
    `Van\u2019t Riet coalescing correlation assumed. Non-coalescing media would yield ` +
      `higher kLa at the same P/V, reducing OTR risk.`,
  );

  if (inputs.impeller_type === "unknown") {
    assumptions.push(
      `Unknown impeller type defaulted to Rushton constants (Np = 5.0, Pg/P = 0.60). ` +
        `Actual power draw may differ, affecting all scale-up projections.`,
    );
  }

  return assumptions.slice(0, 3);
}

function getSingleMeasurementAdvice(inputs: ProcessInputs): string {
  if (inputs.our_mode === "estimate") {
    return "A direct OUR measurement at peak biomass would most improve this assessment.";
  }
  return "A kLa characterisation of the target vessel at operating conditions would most improve this assessment.";
}

// --- Mitigation text (reused from dashboard logic) ---

function getMitigation(
  domain: string,
  results: PartialAssessmentResult,
  inputs: ProcessInputs,
): string | null {
  const { otr, mixing, shear, co2, heat } = results;
  const score =
    domain === "otr" ? otr.score :
    domain === "mixing" ? mixing.score :
    domain === "shear" ? shear.score :
    domain === "co2" ? co2.score :
    heat.score;

  if (score !== "high" && score !== "critical") return null;

  switch (domain) {
    case "otr":
      return `Required kLa of ${fmt(otr.kla_required)} h\u207B\u00B9 exceeds standard P/V achievability at ${fmtInt(inputs.v_target)} L. Options: (1) Reduce target biomass ~20% to bring OUR within standard kLa range. (2) Dual Rushton or Rushton-to-PBT cascade configuration. (3) Pressure overlay (+0.5 bar increases C* by ~15%).`;
    case "mixing":
      if (mixing.da != null) {
        return `Mixing time at ${fmtInt(inputs.v_target)} L is ~${fmt(mixing.theta_mix_target)} s. Feed produces Da = ${fmt(mixing.da, 3)}. Options: (1) Switch to continuous feed. (2) Relocate feed port to impeller high-turbulence zone. (3) Reduce peak feed rate and extend feed duration.`;
      }
      return `Mixing time at ${fmtInt(inputs.v_target)} L is ~${fmt(mixing.theta_mix_target)} s. Options: (1) Add a second impeller. (2) Reduce target vessel H/D ratio. (3) Increase agitation speed within shear limits.`;
    case "shear":
      return `Tip speed of ${fmt(shear.tip_speed)} m/s at constant P/V exceeds ${SPECIES_LABELS[inputs.organism_species] ?? inputs.organism_species} threshold of ${fmt(shear.tip_speed_threshold)} m/s. Options: (1) Switch to pitched blade turbine. (2) Accept lower P/V with supplementary O\u2082 strategy. (3) Consider scale-up at constant tip speed instead of constant P/V.`;
    case "co2":
      return `Estimated pCO\u2082 at vessel bottom is ${fmt(co2.pco2_bottom ?? 0, 2)} bar. Options: (1) Increase sparging rate to enhance CO\u2082 stripping. (2) Reduce biomass target. (3) Lower H/D vessel geometry.`;
    case "heat":
      return `Metabolic heat generation of ${fmt(heat.q_metabolic)} kW exceeds jacket cooling capacity of ${fmt(heat.q_cool_max)} kW. Options: (1) Add internal cooling coil. (2) Reduce target biomass or OUR. (3) Lower process temperature if organism permits.`;
    default:
      return null;
  }
}

// --- Pilot computation (mirrors dashboard logic) ---

interface PilotRow {
  klaAchievable: number;
  mixingTime: number;
  da: number | null;
  tipSpeed: number;
  pco2Bottom: number | null;
  heatKwM3: number;
}

function computePilot(inputs: ProcessInputs, derived: DerivedParameters): PilotRow {
  const vPilot = pilotVolume(inputs.v_lab, inputs.v_target);
  const geometry = deriveVesselGeometry(vPilot, inputs.h_d_target, inputs.impeller_type);
  const gas = deriveGasVelocity(inputs.vvm, vPilot, geometry.a_cross);

  const klaAchievable = klaVantRiet(derived.pv_lab, gas.vs);

  const pilotT = geometry.t_diameter;
  const pilotDimp = geometry.d_imp;
  const epsilon = derived.pv_lab / RHO;
  const mixingTime =
    RUSZKOWSKI_CONSTANT * pilotT * pilotT /
    (Math.pow(epsilon, 1 / 3) * Math.pow(pilotDimp, 4 / 3));

  let da: number | null = null;
  if (inputs.process_type === "fed_batch" && inputs.feed_frequency) {
    const tau = FEED_TAU_MAP[inputs.feed_frequency];
    da = mixingTime / tau;
  }

  const dImpLab = derived.lab_geometry.d_imp;
  const nTarget = derived.n_rps * Math.pow(dImpLab / pilotDimp, 5 / 3);
  const tipSpeed = Math.PI * nTarget * pilotDimp;

  let pco2Bottom: number | null = null;
  const activated =
    derived.biomass_cdw > CO2_BIOMASS_THRESHOLD || derived.our_peak > CO2_OUR_THRESHOLD;
  if (activated) {
    const rq =
      inputs.organism_species === "p_pastoris"
        ? RQ_DEFAULTS.p_pastoris_methanol
        : inputs.organism_species === "s_cerevisiae"
          ? RQ_DEFAULTS.s_cerevisiae_aerobic
          : RQ_DEFAULTS.bacteria_aerobic;
    const cer = rq * derived.our_peak;
    const klaO2Pilot = klaVantRiet(derived.pv_lab, gas.vs);
    const klaCo2 = KLA_CO2_O2_RATIO * klaO2Pilot;
    const pco2BulkAtm = (cer / 1000) / (klaCo2 * H_CO2);
    const pco2Bulk = pco2BulkAtm * 1.01325;
    const hLiquid = geometry.h_liquid;
    const dpHydro = RHO * G * hLiquid;
    pco2Bottom = pco2Bulk + (dpHydro / ATMOSPHERIC_PRESSURE_PA) * 1.01325;
  }

  const vPilotM3 = geometry.volume_m3;
  const qMetabolic = METABOLIC_HEAT_FACTOR * derived.our_peak * vPilotM3;
  const heatKwM3 = vPilotM3 > 0 ? qMetabolic / vPilotM3 : 0;

  return { klaAchievable, mixingTime, da, tipSpeed, pco2Bottom, heatKwM3 };
}

